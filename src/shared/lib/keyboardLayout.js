// Перевод текста, случайно набранного в русской раскладке, в латиницу
// по физическому расположению клавиш (ЙЦУКЕН → QWERTY).
// Нужно для поиска по доменам: домены латиницей, а пользователь
// мог забыть переключить раскладку и набрать «ешьуцуи» вместо «timeweb».
const RU_TO_EN = {
  й: 'q', ц: 'w', у: 'e', к: 'r', е: 't', н: 'y', г: 'u', ш: 'i', щ: 'o', з: 'p',
  х: '[', ъ: ']', ф: 'a', ы: 's', в: 'd', а: 'f', п: 'g', р: 'h', о: 'j', л: 'k',
  д: 'l', ж: ';', э: "'", я: 'z', ч: 'x', с: 'c', м: 'v', и: 'b', т: 'n', ь: 'm',
  б: ',', ю: '.', ё: '`',
}

// Заменяет кириллические символы на латинские по раскладке.
// Прочие символы (латиница, цифры, дефис, точка) остаются как есть.
export function ruLayoutToEn(str) {
  let out = ''
  for (const ch of str) {
    out += RU_TO_EN[ch] ?? ch
  }
  return out
}

// Есть ли в строке кириллица — чтобы не тратить силы на перевод латиницы.
export function hasCyrillic(str) {
  return /[а-яё]/i.test(str)
}
